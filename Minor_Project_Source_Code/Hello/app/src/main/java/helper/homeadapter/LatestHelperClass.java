package helper.homeadapter;

public class LatestHelperClass {
    int image;
    String title;
    String description;

    public LatestHelperClass(int image, String title, String description) {
        this.image = image;
        this.title = title;
        this.description = description;
    }

    public int getImage() {
        return image;
    }

    public String getTitle() {
        return title;
    }

    public String getDescription() {
        return description;
    }
}
